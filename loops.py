# While loop
# Incremanting
number = 20
while number <= 25:
    print(number)
    number += 1

# Decrementing
num = 105
while num >= 100:
    print(num)
    num -= 1

# Break statement
x = 30
while x <= 40:
    print(x)
    if x == 36:
        break
    x += 1
# Continue
y = 30
while y <= 35:
    y += 1
    if y == 32:
        continue
        print(y)

# For loop
languages = ["Python", "Java","C++"]
for lang in languages:
    print(lang)

# Range
for mynumber in range(6):
    print(mynumber)

for mynum in range(20,28):
    print(mynum)

for z in range (20,30,2):
    print(z)
